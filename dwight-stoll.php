			Following graduate in 2007, he spent nine months working as a post-doctoral fellow with Dr. Christine Wendt in the Lung Health Center at the University of Minnesota, where he began analyzing the low molecular weight constituents of human lung lavage fluid using liquid chromatography coupled with mass spectrometry. In the fall of 2008, Dwight began his faculty career as assistant professor in the chemistry department at Gustavus Adolphus College, where he mainly teaches quantitative and instrumental analysis courses, in addition to directing a vibrant research program involving mainly undergraduate students. His active research projects include the development of rapid multidimensional liquid chromatography for both targeted and untargeted analysis in complex matrices, optimization of isocratic and gradient elution HPLC, and the characterization and role of selectivity in reversed-phase HPLC.Dwight is the author or co-author of 35 peer-reviewed publications and two book chapters in the area of separation science. He has authored or co-authored over 80 presentations at local, national, and international meetings. In 2009 he was the winner of the John B. Phillips Award for contributions to multidimensional gas chromatography, and in 2011 he was the recipient of LCGC's Emerging Leader in Chromatography Award. Most recently he was recently elected to the editorial advisory boards of LCGC Magazine, the leading trade publication for the separation science community, and the separation science journal Chromatographia.
