			Dwight Stoll did his undergraduate work at Minnesota State University, Mankato, receiving B.S. degrees in plant biology and biochemistry in 1999 and 2001. Upon graduation in 1999 he took a job in industry as a research technician with ZirChrom Separations, Inc. At ZirChrom he quickly learned about the liquid chromatography market, and in fact became quite interested in the role of separation science in the development of new analytical methodologies for use in other disciplines such as biology. In 2000 he shored up his chemistry background at the University of Minnesota before enrolling in the graduate program in chemistry there in 2001. At the UofM he studied with Professor Peter Carr, and worked on the development of Fast, Comprehensive Two-Dimensional Liquid Chromatography, using the principles of high temperature and ultra-fast gradient elution liquid chromatography to improve the overall speed of two-dimensional separations. Before receiving the Ph.D. in analytical chemistry in 2007, he took a nine-month break from graduate studies to teach as an adjunct faculty member at St. Olaf College where he taught analytical and general chemistry.
